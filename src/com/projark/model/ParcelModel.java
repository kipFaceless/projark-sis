
package com.projark.model;


public class ParcelModel {
    int proj_id;
    int parcelOrder;
    float projectPrice;
    float totalAfter;
    float parcelValue;
    int parcelQty;
    String payDate;

    public String getPayDate() {
        return payDate;
    }

    public void setPayDate(String payDate) {
        this.payDate = payDate;
    }

    public int getProj_id() {
        return proj_id;
    }

    public void setProj_id(int proj_id) {
        this.proj_id = proj_id;
    }

    public int getParcelOrder() {
        return parcelOrder;
    }

    public void setParcelOrder(int parcelOrder) {
        this.parcelOrder = parcelOrder;
    }

    public float getProjectPrice() {
        return projectPrice;
    }

    public void setProjectPrice(float projectPrice) {
        this.projectPrice = projectPrice;
    }

    public float getTotalAfter() {
        return totalAfter;
    }

    public void setTotalAfter(float totalAfter) {
        this.totalAfter = totalAfter;
    }

    public float getParcelValue() {
        return parcelValue;
    }

    public void setParcelValue(float parcelValue) {
        this.parcelValue = parcelValue;
    }

    public int getParcelQty() {
        return parcelQty;
    }

    public void setParcelQty(int parcelQty) {
        this.parcelQty = parcelQty;
    }

   
    
    
    
    
}
